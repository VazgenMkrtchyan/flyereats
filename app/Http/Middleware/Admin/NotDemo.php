use Closure;

class NotDemo {

	/**
	 * Handle an incoming request.
	 *
	 * @param  \Illuminate\Http\Request  $request
	 * @param  \Closure  $next
	 * @return mixed
	 */
	public function handle($request, Closure $next)
	{
		//disables some actions
		if (demo_mode_on()) {

			flash()->error(trans('back.flash_action_disabled_in_demo'));
			return back();

		}

		return $next($request);
	}

}
